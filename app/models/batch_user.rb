class BatchUser < ApplicationRecord
  STATUS = { accepted: 0, requested: 1, rejected: 2}
  # ----------------------------------------------------------------------------------------------------
  # ASSOCIATION
  # ----------------------------------------------------------------------------------------------------
  belongs_to :batch, optional: false
  belongs_to :user, optional: false
  
  # --------------------------------------------------------------------------------------------------------
  # SCOPE
  # --------------------------------------------------------------------------------------------------------
  scope :active, -> { where(active: true) }

  # --------------------------------------------------------------------------------------------------------
  # ENUM
  # --------------------------------------------------------------------------------------------------------
  enum status: STATUS
end
